class TemperatureScaling(nn.Module):
    """
    Post-hoc calibration wrapper.
    FIX #4: temperature initialized to 1.0 (neutral) instead of 1.5.
    """
    def __init__(self, model):
        super().__init__()
        self.model       = model
        self.temperature = nn.Parameter(torch.ones(1, device=DEVICE) * 1.0)

    def forward(self, x):
        return self.model(x) / self.temperature

    def calibrate(self, val_loader, device=DEVICE):
        """Fit temperature on validation set using L-BFGS."""
        self.to(device)
        self.model.eval()
        nll = nn.CrossEntropyLoss()

        all_logits, all_labels = [], []
        with torch.no_grad():
            for xb, yb in val_loader:
                all_logits.append(self.model(xb.to(device)).cpu())
                all_labels.append(yb)
        all_logits = torch.cat(all_logits)
        all_labels = torch.cat(all_labels)

        T = self.temperature.detach().cpu().requires_grad_(True)
        optimizer = torch.optim.LBFGS([T], lr=0.1, max_iter=100,
                                       line_search_fn='strong_wolfe')
        def eval_fn():
            optimizer.zero_grad()
            loss = nll(all_logits / T, all_labels)
            loss.backward()
            return loss

        optimizer.step(eval_fn)
        self.temperature.data = T.detach().clamp(0.1, 10.0).to(device)
        print(f'Learned temperature T = {self.temperature.item():.4f}')
        if self.temperature.item() > 1:
            print('  T > 1 → predictions were overconfident → softened')
        else:
            print('  T ≤ 1 → predictions were underconfident → sharpened')
        return self


