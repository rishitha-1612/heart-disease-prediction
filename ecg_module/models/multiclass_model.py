"""## 10 — FIXED Training Loop

**FIX #2**: Removed label smoothing — Focal Loss only. Label smoothing and Focal Loss together double-penalize confidence: Focal Loss down-weights easy correct predictions, while label smoothing caps the target probability at ~(1 - ε). The result is a model that classifies correctly but never learns to be confident. Temperature Scaling handles calibration post-hoc.
"""

def train_model(model, train_loader, val_loader,
                criterion,
                model_name='Model', save_path='best.pth',
                lr=1e-3, weight_decay=1e-4,
                num_epochs=35, patience=8):
    """
    FIX #2: Focal Loss only — label smoothing removed.
    """
    model.to(DEVICE)

    optimizer = Adam(model.parameters(), lr=lr, weight_decay=weight_decay)
    scheduler = OneCycleLR(optimizer, max_lr=lr,
                           steps_per_epoch=len(train_loader),
                           epochs=num_epochs, pct_start=0.3,
                           anneal_strategy='cos', div_factor=10.0,
                           final_div_factor=100.0)

    best_val_loss = float('inf')
    patience_cnt  = 0
    best_weights  = copy.deepcopy(model.state_dict())
    history       = {k: [] for k in ['train_loss','val_loss','train_acc','val_acc']}

    print(f'\nTraining {model_name} on {DEVICE} …')
    print(f'{"Epoch":>6}  {"TrLoss":>7}  {"TrAcc":>6}  {"VaLoss":>7}  {"VaAcc":>6}  Status')
    print('-'*58)

    for epoch in range(1, num_epochs + 1):
        # ── Train ──────────────────────────────────────────────────────
        model.train()
        tloss, tcorr, ttot = 0.0, 0, 0
        for xb, yb in train_loader:
            xb, yb = xb.to(DEVICE), yb.to(DEVICE)
            logits = model(xb)
            loss   = criterion(logits, yb)   # FIX #2: Focal Loss only
            optimizer.zero_grad()
            loss.backward()
            nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            scheduler.step()
            tloss += loss.item() * xb.size(0)
            tcorr += (logits.argmax(1) == yb).sum().item()
            ttot  += xb.size(0)

        # ── Validate ───────────────────────────────────────────────────
        model.eval()
        vloss, vcorr, vtot = 0.0, 0, 0
        with torch.no_grad():
            for xb, yb in val_loader:
                xb, yb = xb.to(DEVICE), yb.to(DEVICE)
                logits  = model(xb)
                vloss  += criterion(logits, yb).item() * xb.size(0)
                vcorr  += (logits.argmax(1) == yb).sum().item()
                vtot   += xb.size(0)

        tl, ta = tloss/ttot, tcorr/ttot
        vl, va = vloss/vtot, vcorr/vtot
        history['train_loss'].append(tl)
        history['val_loss'].append(vl)
        history['train_acc'].append(ta)
        history['val_acc'].append(va)

        status = ''
        if vl < best_val_loss - 1e-4:
            best_val_loss = vl
            best_weights  = copy.deepcopy(model.state_dict())
            patience_cnt  = 0
            status        = '✓ saved'
            torch.save(best_weights, save_path)
        else:
            patience_cnt += 1
            if patience_cnt >= patience:
                print(f'  Early stop at epoch {epoch} (patience={patience})')
