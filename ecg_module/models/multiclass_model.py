"""## 14 — ECE Helper + Full Evaluation Suite"""

def expected_calibration_error(y_true, y_prob, n_bins=10):
    """ECE — lower is better. 0 = perfect calibration."""
    confidences = y_prob.max(axis=1)
    predictions = y_prob.argmax(axis=1)
    correct     = (predictions == y_true).astype(float)
    bins        = np.linspace(0.0, 1.0, n_bins + 1)
    ece         = 0.0
    for i in range(n_bins):
        mask = (confidences > bins[i]) & (confidences <= bins[i+1])
        if mask.sum() == 0: continue
        ece += abs(correct[mask].mean() - confidences[mask].mean()) * (mask.sum() / len(y_true))
    return ece


@torch.no_grad()
def get_preds(model, loader):
    model.eval()
    all_true, all_pred, all_prob = [], [], []
    for xb, yb in loader:
        prob = F.softmax(model(xb.to(DEVICE)), dim=1).cpu().numpy()
        all_prob.extend(prob)
        all_pred.extend(prob.argmax(axis=1))
        all_true.extend(yb.numpy())
    return (np.array(all_true, dtype=int),
            np.array(all_pred, dtype=int),
            np.array(all_prob,  dtype=float))


def full_evaluate(model, loader, model_name='Model', n_bins=10):
    y_true, y_pred, y_prob = get_preds(model, loader)

    acc = accuracy_score(y_true, y_pred)
    try:
        roc = roc_auc_score(y_true, y_prob, multi_class='ovr', average='macro')
    except ValueError:
        roc = float('nan')
    ece    = expected_calibration_error(y_true, y_prob, n_bins)
    report = classification_report(y_true, y_pred, target_names=CLASSES, digits=4, zero_division=0)
    cm     = confusion_matrix(y_true, y_pred)

    conf_by_class = {}
    max_conf = y_prob.max(axis=1)
    for i, cls in enumerate(CLASSES):
        mask = y_true == i
        conf_by_class[cls] = max_conf[mask].mean() if mask.sum() > 0 else float('nan')

    print(f"\n{'═'*60}")
    print(f'  {model_name}')
    print(f"{'═'*60}")
    print(f'  Accuracy       : {acc:.4f}  ({acc*100:.2f}%)')
    print(f'  ROC-AUC (macro): {roc:.4f}')
    print(f'  ECE            : {ece:.4f}  (lower = better calibrated)')
    print(f'  Mean confidence: {max_conf.mean():.4f}')
    print(f'\n  Per-class mean confidence:')
    for cls, c in conf_by_class.items():
        print(f'    {cls:<6}: {c:.4f}')
    print(f'\n{report}')

    return dict(y_true=y_true, y_pred=y_pred, y_prob=y_prob,
                accuracy=acc, roc_auc=roc, ece=ece, cm=cm,
                conf_by_class=conf_by_class, max_conf=max_conf)


