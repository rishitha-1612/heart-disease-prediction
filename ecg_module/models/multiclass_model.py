"""## 9 — FIXED Focal Loss

**FIX #1**: Correct `p_t` computed from `log_softmax` — decoupled from alpha.

**FIX #3**: Class weights from original `y_train`, not oversampled `y_train_bal`.

**FIX #14 (NEW)**: Removed redundant `F.cross_entropy` call. The original code computed `log_softmax` once to get `log_pt`, then called `F.cross_entropy` which internally runs `log_softmax` again. Since `CE = -log_pt`, we now use `-log_pt` directly — one softmax pass.
"""

class FocalLoss(nn.Module):
    """
    FIXED Focal Loss: FL(p_t) = -alpha_t * (1 - p_t)^gamma * log(p_t)

    FIX #1 : p_t from log_softmax, completely decoupled from alpha.
    FIX #14: ce = -log_pt directly. Eliminates redundant internal softmax
             that F.cross_entropy() was computing on every forward pass.
    """
    def __init__(self, alpha=None, gamma=2.0, reduction='mean'):
        super().__init__()
        self.alpha     = alpha    # (C,) tensor on DEVICE
        self.gamma     = gamma
        self.reduction = reduction

    def forward(self, logits, targets):
        # Step 1: log-probabilities (one softmax pass)
        log_prob = F.log_softmax(logits, dim=1)                       # (B, C)

        # Step 2: true-class log-probability and probability
        log_pt = log_prob.gather(1, targets.unsqueeze(1)).squeeze(1)  # (B,)
        p_t    = log_pt.exp()                                         # (B,) ← CORRECT p_t

        # Step 3: cross-entropy = -log_pt  (FIX #14: no second softmax pass)
        ce = -log_pt                                                   # (B,)

        # Step 4: focal modulation
        focal_weight = (1.0 - p_t) ** self.gamma                      # (B,)

        # Step 5: per-sample alpha weight
        if self.alpha is not None:
            alpha_t = self.alpha[targets]                             # (B,)
            fl = alpha_t * focal_weight * ce
        else:
            fl = focal_weight * ce

        if self.reduction == 'mean': return fl.mean()
        if self.reduction == 'sum':  return fl.sum()
        return fl


