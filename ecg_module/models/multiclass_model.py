"""## 8 — ECG_CNN_Attention Model (architecture unchanged — correct)"""

class SEBlock(nn.Module):
    def __init__(self, channels, reduction=8):
        super().__init__()
        mid = max(channels // reduction, 4)
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc   = nn.Sequential(
            nn.Linear(channels, mid, bias=False),
            nn.ReLU(inplace=True),
            nn.Linear(mid, channels, bias=False),
            nn.Sigmoid(),
        )
    def forward(self, x):
        w = self.pool(x).squeeze(-1)
        w = self.fc(w).unsqueeze(-1)
        return x * w


class ResConvBlock(nn.Module):
    def __init__(self, in_ch, out_ch, kernel=7, pool=2, drop=0.3):
        super().__init__()
        pad = kernel // 2
        self.conv = nn.Sequential(
            nn.Conv1d(in_ch,  out_ch, kernel, padding=pad),
            nn.BatchNorm1d(out_ch),
            nn.GELU(),
            nn.Conv1d(out_ch, out_ch, kernel, padding=pad),
            nn.BatchNorm1d(out_ch),
        )
        self.se      = SEBlock(out_ch)
        self.proj    = nn.Conv1d(in_ch, out_ch, 1) if in_ch != out_ch else nn.Identity()
        self.act     = nn.GELU()
        self.pool    = nn.MaxPool1d(pool, stride=pool)
        self.dropout = nn.Dropout(p=drop)

    def forward(self, x):
        residual = self.proj(x)
        out      = self.se(self.conv(x)) + residual
        out      = self.act(out)
        out      = self.dropout(self.pool(out))
        return out


class TemporalAttentionPool(nn.Module):
    def __init__(self, channels):
        super().__init__()
        self.attn = nn.Sequential(
            nn.Conv1d(channels, channels // 4, kernel_size=1),
            nn.Tanh(),
            nn.Conv1d(channels // 4, 1, kernel_size=1),
        )
    def forward(self, x):
        w = torch.softmax(self.attn(x), dim=-1)
        return (x * w).sum(dim=-1)


class ECG_CNN_Attention(nn.Module):
    def __init__(self, num_leads=NUM_LEADS, num_classes=NUM_CLASSES, drop=0.3):
        super().__init__()
        self.features = nn.Sequential(
            ResConvBlock(num_leads,  32,  kernel=11, pool=2, drop=drop),
            ResConvBlock(32,         64,  kernel=7,  pool=2, drop=drop),
            ResConvBlock(64,        128,  kernel=5,  pool=2, drop=drop),
            ResConvBlock(128,       256,  kernel=3,  pool=2, drop=drop),
            ResConvBlock(256,       256,  kernel=3,  pool=2, drop=drop),
        )
        self.pool       = TemporalAttentionPool(256)
        self.classifier = nn.Sequential(
            nn.Linear(256, 128),
            nn.GELU(),
            nn.Dropout(drop),
            nn.Linear(128, num_classes),
        )

    def forward(self, x):
        return self.classifier(self.pool(self.features(x)))

    def get_attention_weights(self, x):
        f   = self.features(x)
        raw = self.pool.attn(f)
        return torch.softmax(raw, dim=-1).squeeze(1)


