"""## 19 — Sample Predictions with Calibrated Confidence

**FIX #11 (NEW)**: `predict()` now accepts `train_mean` and `train_std` as explicit parameters instead of reading them from the global scope. The original function silently failed whenever called outside the notebook session (e.g. after loading from checkpoint) because the global variables no longer existed. Now the function is fully self-contained and portable.
"""

RISK_MAP = {'NORM': 'Low', 'MI': 'High', 'STTC': 'Medium', 'CD': 'Medium', 'HYP': 'Medium'}

@torch.no_grad()
def predict(model, ecg: np.ndarray,
            norm_mean: np.ndarray, norm_std: np.ndarray,
            already_preprocessed: bool = True) -> dict:
    """
    Return a calibrated prediction dict for a single ECG array.

    FIX #11: norm_mean and norm_std are now explicit parameters, not globals.
    This makes the function portable — it works correctly whether called inside
    the notebook, in a fresh session, or in a deployment script, as long as the
    caller loads these arrays from the saved checkpoint.

    Args:
        model              : trained (optionally calibrated) ECG_CNN_Attention
        ecg                : numpy array of shape (12, 1000) — preprocessed & normalised,
                             OR raw wfdb output of shape (time, 12) if already_preprocessed=False
        norm_mean          : per-lead mean array, shape (1, 12, 1) or (12, 1)
        norm_std           : per-lead std  array, shape (1, 12, 1) or (12, 1)
        already_preprocessed: if False, runs bandpass + resample + z-score normalisation

    Returns:
        dict with keys: predicted_class, score, level, probabilities
    """
    if not already_preprocessed:
        ecg = preprocess(ecg)
        mean = norm_mean.squeeze(0) if norm_mean.ndim == 3 else norm_mean
        std  = norm_std.squeeze(0)  if norm_std.ndim  == 3 else norm_std
        ecg  = (ecg - mean) / std

    x    = torch.from_numpy(ecg).float().unsqueeze(0).to(DEVICE)
    prob = F.softmax(model(x), dim=1).squeeze(0).cpu().numpy()
    idx  = int(np.argmax(prob))
    pred = CLASSES[idx]
    conf = round(float(prob[idx]), 6)
    return {
        'level':           RISK_MAP.get(pred, 'Medium'),
        'score':           conf,
        'predicted_class': pred,
        'probabilities':   {c: round(float(p), 6) for c, p in zip(CLASSES, prob)},
    }


