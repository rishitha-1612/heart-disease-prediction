class ECGAugment:
    """ECG-specific augmentation suite. All methods use torch.rand for DataLoader worker safety."""

    @staticmethod
    def gaussian_noise(x, sigma=0.025):
        return x + sigma * torch.randn_like(x)

    @staticmethod
    def amplitude_scale(x, lo=0.8, hi=1.2):
        s = lo + (hi - lo) * torch.rand(1).item()
        return x * s

    @staticmethod
    def time_shift(x, max_shift=50):
        s = int((torch.rand(1).item() - 0.5) * 2 * max_shift)
        x = torch.roll(x, s, dims=1)
        if s > 0:   x[:, :s]  = 0.0
        elif s < 0: x[:, s:]  = 0.0
        return x

    @staticmethod
    def magnitude_warp(x, sigma=0.15, n_knots=4):
        T  = x.shape[1]
        kx = torch.linspace(0, T - 1, n_knots + 2)
        # FIX #8 (partial): use torch instead of np.random so worker RNG is independent
        ky = 1.0 + sigma * torch.randn(n_knots + 2)
        # Simple linear interp via torch — avoids numpy RNG entirely
        t  = torch.arange(T, dtype=torch.float32)
        w  = torch.zeros(T)
        for i in range(len(kx) - 1):
            mask = (t >= kx[i]) & (t < kx[i + 1])
            if mask.any():
                frac = (t[mask] - kx[i]) / (kx[i + 1] - kx[i])
                w[mask] = ky[i] * (1 - frac) + ky[i + 1] * frac
        w[-1] = ky[-1]
        w = w.clamp(0.5, 1.5).unsqueeze(0)   # (1, T)
        return x * w

    @staticmethod
    def baseline_wander(x, fs=FS):
        T    = x.shape[1]
        t    = torch.linspace(0, T / fs, T)
        # FIX #8 (partial): use torch.rand instead of np.random.uniform
        freq = 0.1 + 0.4 * torch.rand(1).item()
        amp  = 0.03 + 0.09 * torch.rand(1).item()
        w    = (amp * torch.sin(2 * 3.14159 * freq * t)).unsqueeze(0)
        return x + w

    @classmethod
    def augment(cls, x: torch.Tensor, p: float = 0.5) -> torch.Tensor:
        if torch.rand(1) < p: x = cls.gaussian_noise(x)
        if torch.rand(1) < p: x = cls.amplitude_scale(x)
        if torch.rand(1) < p: x = cls.time_shift(x)
        if torch.rand(1) < p: x = cls.magnitude_warp(x)
        if torch.rand(1) < p: x = cls.baseline_wander(x)
        return x


def oversample_minority(X, y, target_ratio=0.65):
    """
    Oversample minority classes by simple duplication (NO augmentation here).
    FIX #5: Removed ECGAugment.augment() from inside oversampling.
    """
    counts     = np.bincount(y, minlength=NUM_CLASSES)
    target_cnt = int(counts.max() * target_ratio)
    print(f'Oversampling target per class: {target_cnt}')

    X_extra, y_extra = [], []
    for cls_idx in range(NUM_CLASSES):
        cls_mask = np.where(y == cls_idx)[0]
        deficit  = target_cnt - len(cls_mask)
        if deficit <= 0:
            print(f'  {CLASSES[cls_idx]:<6}: {len(cls_mask):>5} → no oversampling needed')
            continue
        chosen = np.random.choice(cls_mask, size=deficit, replace=True)
        X_extra.append(X[chosen])
