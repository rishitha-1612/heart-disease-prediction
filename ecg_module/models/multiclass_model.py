"""## 3 — Preprocessing Pipeline"""

def bandpass_filter(ecg, fs=FS, lowcut=0.5, highcut=40.0, order=4):
    nyq  = 0.5 * fs
    low  = max(lowcut / nyq, 1e-4)
    high = min(highcut / nyq, 1 - 1e-4)
    b, a = sp_signal.butter(order, [low, high], btype='band')
    return np.stack([sp_signal.filtfilt(b, a, ecg[i]) for i in range(ecg.shape[0])], axis=0)

def preprocess(ecg_raw, fs=FS):
    ecg = ecg_raw.T.astype(np.float32)          # (leads, time)
    ecg = np.nan_to_num(ecg)
    ecg = bandpass_filter(ecg, fs)
    if ecg.shape[1] != TARGET_LEN:
        ecg = sp_signal.resample(ecg, TARGET_LEN, axis=1)
    return ecg.astype(np.float32)

